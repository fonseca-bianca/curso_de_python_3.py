print(12, 34, sep="-")
#sep: Separador. Refere-se ao argumento nomeado
#end: Finalizador. Refere-se ao argumento nomeado
print(12, 34, sep="-", end="*")
print(56, 78, sep='.', end='##')
print()
print()
#\r: Caractere de retorno. Faz com que o cursor volte para o início da linha.
print("Olá, mundo", end="\r")
# output: 9.0, mundo

#\n: Caractere de nova linha. Faz com que o cursor vá para a linha seguinte.
print(9, 0, sep='.')
print(9, 0, sep=' ')
