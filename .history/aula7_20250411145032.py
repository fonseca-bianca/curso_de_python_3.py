""" 
* Variáveis:
    - São espaços na memória que guardam valores, dados, informações.
    - São nomeados e podem ser manipulados durante a execução do programa.
    - São representados por um identificador (nome da variável).
    - O nome da variável deve começar com letra minúscula ou underscore (_).
    - seguee padrão snake_case (letras minúsculas e separadas por underscore)
    - = é o sinal de atribuição de valor à variável

"""
nome = "Maria"
int_idade = 31
maior_idade = int_idade >= 18
print("Nome:", nome, "Idade:", int_idade, "É maior de idade?", maior_idade)