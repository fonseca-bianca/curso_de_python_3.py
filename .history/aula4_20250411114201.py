"""
* Tipos int e float:
    - int = inteiro: são números inteiros, positivos ou negativos.
    - int SEM sinal é positivo
    - float = ponto flutuante: são números reais, positivos ou negativos.
    - float SEM sinal é positivo
* Função type(): retorna o tipo do dado com base no valor colocado
"""
