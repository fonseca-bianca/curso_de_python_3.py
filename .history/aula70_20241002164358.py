"""
Retorno de valores das funções (return)

print(): é uma função que exibe coisas, valores pra saída do sistema (mostra na tela)
NÃO tem valor (é None)
"""

def soma(x, y):
    return x + y

soma1 = soma(1, 1)
soma2 = soma(2, 2)

print(soma1 + soma2) 
""" o código irá retornar a soma das duas variáveis soma1 e soma2, pq tem o 'return'
acima somando os valores de 'x' e 'y' """
